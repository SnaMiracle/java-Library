    private void btnIssueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueActionPerformed
        int _book_id = (int)spBookId.getValue();
        int _member_id = (int)spMemberID.getValue();
        String note = txtNote.getText();
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        
        try{
            String _issue_date = dateFormat.format(dateIssue.getDate());
            String _return_date = dateFormat.format(dateReturn.getDate());
            
            Date issDate = dateFormat.parse(_issue_date);
            Date rtnDate = dateFormat.parse(_return_date);
            
            if(!book_exist){
                JOptionPane.showMessageDialog(null, "You need to check if this book exist first by clicking Search","Check if the book exist",2);
            }else if(!member_exist){
                JOptionPane.showMessageDialog(null, "You need to check if this member exist first by clicking Search","Check if the member exist",2);
            }else if(!issue.checkBookAvailability(_book_id)){
                JOptionPane.showMessageDialog(null, "This book is not Available right now","Book not Available",2);
            }
            else if(rtnDate.before(issDate)){
                JOptionPane.showMessageDialog(null, "The return Date must be after Issue date","Wrong Return Date",2);
            }else{
                issue.addIssue(_book_id, _member_id, "issued", _issue_date, _return_date, note);
                spBookId.setValue(0);
                spMemberID.setValue(0);
                lblBookName.setText("Book Name");
                lblMemberName.setText("Member Full-Name");
                lblAvailable.setText("Yes-or-No");
                lblAvailable.setForeground(new Color(51,102,255));
                dateIssue.setDate(new Date());
                dateReturn.setDate(new Date());
                book_exist = false;
                member_exist = false;
            }
            
        } catch(HeadlessException | NullPointerException | ParseException ex){
            JOptionPane.showMessageDialog(null, "Select Issue Date & Return Date","Select Date",2);
        }
    }//GEN-LAST:event_btnIssueActionPerformed

