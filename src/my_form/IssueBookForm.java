    private void btnSearchBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchBookActionPerformed
    
        int book_id = (int)spBookId.getValue();
        
        try {
            my_class.Book selectedBook = book.getBookById(book_id);
            
            if(selectedBook != null){
                lblBookName.setText(selectedBook.getName());
                book_exist = true;
                if(issue.checkBookAvailability(book_id)){
                   lblAvailable.setText("Yes");
                   lblAvailable.setForeground(Color.green);
                }else{
                    lblAvailable.setText("No");
                    lblAvailable.setForeground(Color.red);
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "This book doesn't exist","Book Not Found",2);
                lblBookName.setText("Book Name");
                book_exist = false;
                lblAvailable.setText("Yes-or_No");
                lblAvailable.setForeground(new Color(51,51,255));

            }
        } catch (SQLException ex) {
            Logger.getLogger(IssueBookForm.class.getName()).log(Level.SEVERE, null, ex);
        
        }

    }//GEN-LAST:event_btnSearchBookActionPerformed

